
class TreeNode(object):
    def __init__(self,left,right,value):
        self.leftNode = left
        self.rightNode = right
        self.value = value


    def setLeftNode(self,node):
        self.leftNode = node

    def setRightNode(self,node):
        self.rightNode = node

    def setValue(self,val):
        self.value = val








